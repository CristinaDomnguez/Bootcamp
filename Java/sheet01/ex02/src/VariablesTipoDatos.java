public class VariablesTipoDatos {
    public static void main(String[] args){
    int numeroEntero = 3;
    double numeroDecimal = 3.33;
    boolean boleano = true;
    char caracter = 'a';
    String frase = "Esto es un frase";

    System.out.println("Esto es un numero entero: " + numeroEntero);
    System.out.println("Esto es un numero decimal: " + numeroDecimal);
    System.out.println("Esto es un boleano: " + boleano);
    System.out.println("Esto es un caracter: " + caracter);
    System.out.println("Esto es una frase: " + frase);
    }
}
